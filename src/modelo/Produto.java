package modelo;

public class Produto {

	private int produto_id;

	private String nome;
	private String fabricante;
	private String origem;
	private String volume;
	private String descricao;
	private String foto;
	private String ingrediente;
	private String alergico;
	private String teor_alcoolico;
	private String temperatura_ideal;
	private String ibu;
	private String caloria;
	private String carboidrato;

	private int usuario_usuario_id;

	public int getProduto_id() {
		return produto_id;
	}

	public void setProduto_id(int produto_id) {
		this.produto_id = produto_id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getFabricante() {
		return fabricante;
	}

	public void setFabricante(String fabricante) {
		this.fabricante = fabricante;
	}

	public String getOrigem() {
		return origem;
	}

	public void setOrigem(String origem) {
		this.origem = origem;
	}

	public String getVolume() {
		return volume;
	}

	public void setVolume(String volume) {
		this.volume = volume;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getFoto() {
		return foto;
	}

	public void setFoto(String foto) {
		this.foto = foto;
	}

	public String getIngrediente() {
		return ingrediente;
	}

	public void setIngrediente(String ingrediente) {
		this.ingrediente = ingrediente;
	}

	public String getAlergico() {
		return alergico;
	}

	public void setAlergico(String alergico) {
		this.alergico = alergico;
	}

	public String getTeor_alcoolico() {
		return teor_alcoolico;
	}

	public void setTeor_alcoolico(String teor_alcoolico) {
		this.teor_alcoolico = teor_alcoolico;
	}

	public String getTemperatura_ideal() {
		return temperatura_ideal;
	}

	public void setTemperatura_ideal(String temperatura_ideal) {
		this.temperatura_ideal = temperatura_ideal;
	}

	public String getIbu() {
		return ibu;
	}

	public void setIbu(String ibu) {
		this.ibu = ibu;
	}

	public String getCaloria() {
		return caloria;
	}

	public void setCaloria(String caloria) {
		this.caloria = caloria;
	}

	public String getCarboidrato() {
		return carboidrato;
	}

	public void setCarboidrato(String carboidrato) {
		this.carboidrato = carboidrato;
	}

	public int getUsuario_usuario_id() {
		return usuario_usuario_id;
	}

	public void setUsuario_usuario_id(int usuario_usuario_id) {
		this.usuario_usuario_id = usuario_usuario_id;
	}

}
